const modal = document.getElementById('color-modal');
const modalName = document.getElementById('modal-car-name');
const colorOptions = document.getElementById('color-options');
const selectedColorNameEl = document.getElementById('selected-color-name');
const modalAdd = document.getElementById('modal-add');
const modalCancel = document.getElementById('modal-cancel');


function openColorModal(car) {
    currentCar = car;

    modalName.textContent = `Choisir une couleur - ${car.name}`;

    selectedColor = { name: "", hex: "" };
    selectedColorNameEl.textContent = "";

    colorOptions.innerHTML = '';

    availableColors.forEach(color => {
        const div = document.createElement('div');
        div.className = 'color-option';
        div.style.backgroundColor = color.hex;

        div.addEventListener('click', () => {

            document.querySelectorAll('.color-option')
                .forEach(c => c.classList.remove('selected'));

            div.classList.add('selected');

            selectedColor = color;

            selectedColorNameEl.textContent =
                "Couleur sélectionnée : " + color.name;
        });

        colorOptions.appendChild(div);
    });

    modal.classList.remove('hidden');
}


modalCancel.addEventListener('click', () => {
    modal.classList.add('hidden');
});


modalAdd.addEventListener('click', () => {
    if (!selectedColor.hex) {
        alert("Veuillez choisir une couleur");
        return;
    }

    addToCartWithColor(currentCar, selectedColor);
    modal.classList.add('hidden');
});


function addToCartWithColor(car, color) {
    let cart = JSON.parse(localStorage.getItem('cart')) || [];

    const existing = cart.find(
        item => item.id === car.id && item.colorHex === color.hex
    );

    if (existing) {
        existing.quantity = (existing.quantity || 1) + 1;
    } else {
        cart.push({
            ...car,
            colorName: color.name,
            colorHex: color.hex,
            quantity: 1
        });
    }

    localStorage.setItem('cart', JSON.stringify(cart));

    alert(`${car.name} (${color.name}) ajouté à la commande !`);
}


function handleAddToCart(car) {
    openColorModal(car);
}


function displayCars(carsToDisplay) {
    grid.innerHTML = '';

    if (carsToDisplay.length === 0) {
        grid.innerHTML = `
            <p style="grid-column:1/-1;text-align:center;padding:80px;color:#64748b;">
                Aucune voiture trouvée.
            </p>`;
        return;
    }

    carsToDisplay.forEach(car => {
        const card = document.createElement('div');
        card.className = 'product-card';

        card.innerHTML = `
            <img src="${car.image}" class="product-image"
                 onerror="this.src='https:

            <div class="product-info">
                <div class="product-category">${car.category} • ${car.etat}</div>
                <h3 class="product-name">${car.name}</h3>
                <p class="product-description">${car.description}</p>

                <div class="product-footer">
                    <span class="product-price">
                        ${formatPrice(car.price)}
                    </span>

                    <button class="btn-add" data-id="${car.id}">
                        Commander
                    </button>
                </div>
            </div>
        `;

        grid.appendChild(card);
    });

    document.querySelectorAll('.btn-add').forEach(btn => {
        btn.addEventListener('click', () => {
            const car = cars.find(c => c.id === parseInt(btn.dataset.id));
            if (car) handleAddToCart(car);
        });
    });
}

function formatPrice(price) {
    if (currentCurrency === 'DZD') {
        return `${Math.round(price * exchangeRate).toLocaleString('fr-FR')} DA`;
    }

    return `${price.toLocaleString('fr-FR')} €`;
}


function filterAndSearch() {
    const term = searchInput.value.toLowerCase().trim();
    let filtered = cars;

    if (currentFilter !== "Tous") {
        filtered = filtered.filter(c => c.category === currentFilter);
    }

    if (term) {
        filtered = filtered.filter(c =>
            c.name.toLowerCase().includes(term) ||
            c.description.toLowerCase().includes(term)
        );
    }

    displayCars(filtered);
}


searchInput.addEventListener('input', filterAndSearch);

currencyToggleBtn.addEventListener('click', () => {
    currentCurrency = currentCurrency === 'EUR' ? 'DZD' : 'EUR';
    currencyToggleBtn.textContent = currentCurrency === 'EUR'
        ? 'DZD'
        : 'EURO';
    filterAndSearch();
});

filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');

        currentFilter = btn.dataset.category;
        filterAndSearch();
    });
});


displayCars(cars);